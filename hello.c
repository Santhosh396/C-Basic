#include<stdio.h>
void main()
{
	// Variable declaration
	int age;
	float cgpa;
	char initial;
	
	// Initialization
	age = 19;
	cgpa = 8.0;
	initial = 'A';
	
	// Output
	printf("Age of the student is %d\n", age);
	printf("CGPA of the student is %f\n", cgpa);
	printf("Initial of the student is %c\n", initial);
}
